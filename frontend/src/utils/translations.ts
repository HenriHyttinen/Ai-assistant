// Translation helper function
export const t = (key: string, language: string): string => {
  return (translations as any)[language]?.[key] || translations.en[key as keyof typeof translations.en] || key;
};

// Translate achievement name
export const translateAchievementName = (name: string, language: string = 'en'): string => {
  const translationKey = achievementNameMap[name];
  return translationKey ? t(translationKey, language) : name;
};

